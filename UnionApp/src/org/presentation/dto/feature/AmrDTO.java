package org.presentation.dto.feature;

public class AmrDTO {
	private String subject;
	private String amrdate;
	private String amrtime;
	private String detail;
	private String creator;
	private String status;
	private String amrid;
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	public String getAmrdate() {
		return amrdate;
	}
	public void setAmrdate(String amrdate) {
		this.amrdate = amrdate;
	}
	public String getAmrtime() {
		return amrtime;
	}
	public void setAmrtime(String amrtime) {
		this.amrtime = amrtime;
	}
	public String getDetail() {
		return detail;
	}
	public void setDetail(String detail) {
		this.detail = detail;
	}
	public String getCreator() {
		return creator;
	}
	public void setCreator(String creator) {
		this.creator = creator;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getAmrid() {
		return amrid;
	}
	public void setAmrid(String amrid) {
		this.amrid = amrid;
	}	
}
